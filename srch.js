function trGen(...para){
    let tr = document.createElement("tr")
    tr.classList.add("f-data")
    let count = 0
    
    for ( x of [...para]){
        count++;
        if (count == 2){
            let td = document.createElement("td")
            td.innerText = x;
            td.classList.add("nm")
            tr.append(td)
            continue
        }
        if (count == 4){
            let td = document.createElement("td")
            td.innerText = x;
            td.classList.add("enroll")
            tr.append(td)
            continue
        }
        let td = document.createElement("td")
        td.innerText = x;
        tr.append(td)
    }
    tbl.append(tr)
}

